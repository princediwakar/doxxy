CREATE OR REPLACE FUNCTION public.deduct_appointment_credit(appointment_id_param uuid, clinic_id_param uuid, credits_to_deduct integer DEFAULT 1)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
    current_balance INTEGER;
    new_balance INTEGER;
BEGIN
    -- Get current credit balance
    SELECT credit_balance INTO current_balance
    FROM clinic_credits
    WHERE clinic_id = clinic_id_param;
    
    -- If no credits record exists, return false
    IF current_balance IS NULL THEN
        RETURN FALSE;
    END IF;
    
    -- Check if sufficient credits available
    IF current_balance < credits_to_deduct THEN
        RETURN FALSE;
    END IF;
    
    -- Calculate new balance
    new_balance := current_balance - credits_to_deduct;
    
    -- Update clinic credits
    UPDATE clinic_credits
    SET 
        credit_balance = new_balance,
        total_credits_used = total_credits_used + credits_to_deduct,
        updated_at = CURRENT_TIMESTAMP
    WHERE clinic_id = clinic_id_param;
    
    -- Create appointment billing record
    INSERT INTO appointment_billing (
        appointment_id,
        clinic_id,
        billing_type,
        amount,
        credits_used
    ) VALUES (
        appointment_id_param,
        clinic_id_param,
        'credit_deduction',
        10.00,
        credits_to_deduct
    );
    
    RETURN TRUE;
END;
$function$
